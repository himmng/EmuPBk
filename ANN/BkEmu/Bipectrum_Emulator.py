import keras as ks

#======================================================================================================================#

                 #========Loading the ANN model=======#
model = ks.models.load_model('Bk.h5')

#======================================================================================================================#

def Bk_pred(params):
    Bk_pred = model.predict(params)             #======function will return the powerspectrum,given the parameters=====#

    return Bk_pred

