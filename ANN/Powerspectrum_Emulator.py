import keras as ks

#======================================================================================================================#

                 #========Loading the ANN model=======#
model = ks.models.load_model('EMuPk222.h5')

#======================================================================================================================#

def pk_pred(params):
    pk_pred = model.predict(params)             #======function will return the powerspectrum,given the parameters=====#

    return pk_pred

